def seed_knowledge(db: Session):
    if db.query(KnowledgeArticle).count() > 0:
        return
    for item in ARTICLES:
        db.add(
            KnowledgeArticle(
                id=item["id"],
                tag=item["tag"],
                title=item["title"],
                summary=item["summary"],
                content=item["content"],
                published_at=datetime.strptime(item["date"], "%Y-%m-%d"),
            )
        )
    db.commit()
